export const COUNTRIES: Country[] = [
  { code: 'JP', name: '日本', regions: JP_REGIONS },
  { code: 'KR', name: '韓国', regions: KR_REGIONS },
  { code: 'US', name: 'United States', regions: US_REGIONS },
];

export const getCountry = (code: string | null | undefined) =>
  COUNTRIES.find((c) => c.code === code) || null;

export const getRegion = (
  countryCode: string | null | undefined,
  regionCode: string | null | undefined,
) => getCountry(countryCode)?.regions.find((r) => r.code === regionCode) || null;
